				switch (strlen($id)) {
					case 2:
						if (file_exists("images/items/000".$id.".png"))
							return '000'.$id;
						else return 404;
						break;
					case 3:
						if (file_exists("images/items/00".$id.".png"))
							return '00'.$id;
						else return 404;
						break;
					case 4:
						if (file_exists("images/items/0".$id.".png"))
							return '0'.$id;
						else return 404;
						break;
				}
